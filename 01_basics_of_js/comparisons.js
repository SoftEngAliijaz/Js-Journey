/*
Comparisons are:
1. ==
2. !=
3. >=
4. <=
5. >
6. <
*/

// -> Examples

// 1. Equal to (==)
let a = 5;
let b = "5";
console.log(a == b); // true (compares values, not types)

// 2. Not equal to (!=)
let c = 10;
let d = 15;
console.log(c != d); // true (values are not equal)

// 3. Greater than or equal to (>=)
let e = 20;
let f = 20;
console.log(e >= f); // true (values are equal)

// 4. Less than or equal to (<=)
let g = 30;
let h = 25;
console.log(g <= h); // false (g is greater than h)

// 5. Greater than (>)
let i = 100;
let j = 50;
console.log(i > j); // true (i is greater than j)

// 6. Less than (<)
let k = 5;
let l = 10;
console.log(k < l); // true (k is less than l)
